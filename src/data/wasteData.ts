export const getUniqueValues = <T extends keyof WasteData>(field: T): WasteData[T][] => {
  const values = wasteData.map(item => item[field]);
  return [...new Set(values)].sort();
};

export const getDates = (): string[] => {
  return [...new Set(wasteData.map(item => item.date))].sort();
};

export const getMealTypes = (): string[] => {
  return [...new Set(wasteData.map(item => item.mealType))].sort();
};

export const getMessTypes = (): string[] => {
  return [...new Set(wasteData.map(item => item.messType))].sort();
};

export const getWasteData = (date: string, mealType: string, messType: string): WasteData | undefined => {
  return wasteData.find(
    item => 
      item.date === date && 
      item.mealType === mealType && 
      item.messType === messType
  );
};

export const getTotalWasteForDay = (date: string, mealType: string): number => {
  return wasteData
    .filter(item => item.date === date && item.mealType === mealType)
    .reduce((total, item) => total + item.quantity, 0);
};